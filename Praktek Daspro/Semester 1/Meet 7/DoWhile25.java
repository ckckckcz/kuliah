public class DoWhile25 {
    public static void main(String[] args) {
        int x = 0;
        do {
            System.out.println(x);
        } while (++x <= 8);
        System.out.println("Program Berhenti");
        System.out.println("================================");
        int y = 10;
        do {
            System.out.println(y);
        } while (++y <= 8);
        System.out.println("Program Berhenti");
    }
}
