public class perobaanincrement{
    public static void main(String[] args){
        int R = 1;
        //increment
        System.out.println("R: " + R);
        System.out.println("++R: " + ++R);
        System.out.println("R++: " + R++);
        //decrement 
        System.out.println("--R: " + --R);
        System.out.println("R--: " + R--);
        System.out.println("R--: " + R);
    }
}